		<h5 class="media-heading">Appuntamento con Acme Systems</h5>
		<p class="mb-20px">
			Il 18 Dicembre, <?=$its?> e Officine Robotiche ospitano un'occasione imperdibile per scoprire tutti i segreti dei prodotti della Acme Systems.
			<b>Sergio Tanzilli</b> e <b>Roberto Asquini</b>, fondatori e progettisti della societ&agrave;, nonch&egrave; nostri amici di lunga data, mostreranno le funzionalit&agrave; e i particolari costruttivi dei moduli Linux Aria, Arietta, Acqua e delle schede di sviluppo Berta e Terra. <a href="#" class="more-info">Altro...</a>
		</p>
		<div class="hidden event-info">
			<p class="mb-20px">
				Con loro ci sar&agrave; anche <b>Maurizio Scarpa</b>, fondatore della A6 Engineering ed esperto di protocolli di rete. Nel suo intervento Maurizio illustrer&agrave; i moduli radio <b>IOTA</b> sviluppati in collaborazione con Acme Systems. Questi nuovi dispositivi di connessione wireless a media portata per reti mesh si propongono come valida alternativa su protocollo open IP6 ai moduli Lora e Sigfox, basati su protocolli proprietari.
			</p>
			<p class="mb-20px">
				La <a href="http://www.acmesystems.it" target="_new" class="fg-orange">Acme Systems</a> &egrave; una societ&agrave; che disegna e produce <b>schede Linux embedded</b> dal 2005; prodotti made in Italy pensati per il basso consumo, l'alta affidabilit&agrave; e dalla grande versatilit&agrave;. Queste board Linux <b>sono il top</b> per chi ha bisogno di una discreta capacit&agrave; di calcolo e della flessibilit&agrave; nella connettivit&agrave;, sfruttando le potenzialit&agrave; di poter collegare diversi tipi di sensori ed attuatori. Se poi il modulo &egrave; progettato, realizzato e supportato completamente in Italia... ancora meglio!
			</p>
			<p class="mb-20px">
				In pi&ugrave;, in occasione di questo incontro ed in anteprima per noi, saranno presentati i dettagli di progetto della nuova scheda <b>Aria D2</b>, basata sul nuovissimo chip <b>Atmel SAMA5D2x</b> con Cortex A5, e le caratteristiche di routing su rete radio multimaster a 868 Mhz, del nuovissimo protocollo aperto IPv6 ottimizzato per l'IoT ed il mobile IoT, implementato sui moduli IOTA.
			</p>
			<p class="mb-20px">
				Non perdere un'opportunit&agrave; esclusiva per imparare, dalla viva voce di chi le ha progettate, le numerose applicazioni che questi moduli possono avere nel campo della <b>robotica</b>, dell'automazione, del telecontrollo e dell'<b>Internet Of Things</b>.
