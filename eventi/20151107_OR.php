		<div class="hidden event-info">
			<div class="section-title">Robot: questi sconosciuti</div>
			<p class="mb-20px">
				Componenti, circuiti e microcontrollori non sono necessariamente strumenti di ingegneria
				di alto livello. Elettronica, informatica e meccanica possono essere, anche, validi strumenti
				da sfruttare per un fai da te casalingo, un hobby, e soprattutto per dare vita ad una passione.
				Per dare prova che i robot non sono solo esploratori di pianeti lontani, Officine Robotiche vuole
				mostrare le proprie esperienze personali nello studio di questa materia.
			</p>
			<div class="section-title">Il programma</div>
			<p class="mb-20px">
				Dopo un'introduzione generale sulla Robotica amatoriale, la sua storia e le sue potenzialit&agrave;,
				Officine Robotiche presenter&agrave; alcuni casi di studio pratici, per mettere in mostra gli sbocchi
				concreti ed inaspettati che si possono aprire in questo contesto.<br/>
				Ed infine, quale miglior modo per tastare con mano l'immediatezza e la concretezza di un progetto
				robotico, se non se non una dimostrazione pratica? Ci lanceremo in una gara di robot <b>LineFollower</b>,
				l'esempio pi&ugrave; semplice di macchina autonoma, il cui scopo &egrave; seguire un tracciato all'interno di una pista.
			</p>
			<p class="mb-20px">
				L'incontro &egrave; <b>gratuito</b> ed <b>aperto a tutti</b>: giovani, adulti e genitori accompagnati da bambini!<br/>
				Non sono richieste conoscenze di base particolari.<br/>
				&Egrave; nostro piacere invitare anche tutti coloro che hanno gi&agrave; abilit&agrave; di vario livello in materia,
				semplicemente per incontrarci, conoscerci e condividere esperienze, progetti ed idee.<br/>
				Non perdere questa occasione unica per conoscere un nuovo mondo in continua evoluzione!
